void main() {
  // tugas percabangan (branching)
  int nilai = 80;
  if (nilai > 70) {
    print('Nilai A');
  } else if (nilai > 40) {
    print('Nilai B');
  } else if (nilai > 0) {
    print('Nilai C');
  } else {
    print('');
  }
  print('--------');
  // tugas perulangan (looping)
  int i = 1;
  while (i <= 10) {
    print(i);
    i++;
  }
}
